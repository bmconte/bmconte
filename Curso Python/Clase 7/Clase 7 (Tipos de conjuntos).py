# Formas de utilizar los Strings

# Upper -> Convierte todo a Mayúsculas
cadena = "Hola Mundo"
cadena.upper()
"HOLA MUNDO"
"hola amigo!".upper()
"HOLA AMIGO!"

# Lower -> convierte todo en minúsculas
cadena = "Hola Mundo"
cadena.lower()
"hola mundo"
"HOLA AMIGO!".lower()
"hola amigo!"

# Capitalize -> Convierte solo la primera letra de la línea en mayúscula y el resto todo a minúsculas.

# Title -> Convierte solo la primera letra de cada palabra en mayúscula y pone el resto en minúsculas.

# Count -> puede contar cuantas veces existe un dato dentro de una cadena.

